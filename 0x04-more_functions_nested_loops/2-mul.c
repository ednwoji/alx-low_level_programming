/**
 * int mul(int a, int b) - multiplies two integers
 * Description: Multiplies two integers
 * @a: int to be multiplied to b
 * @b: int to be multiplied to a
 * Return: the result of the operation
 */
int mul(int a, int b)
{
	int product;
	product = a * b;
	
	return (product);
}
